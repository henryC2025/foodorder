    /**
     * Add user to system
     * @param username parameter for username
     * @param password parameter for password
     * @param fileName parameter for fileName
     */
     public void addTXTfile(String username, String password, String fileName){
        
         try{
         BufferedWriter writeFile = new BufferedWriter(new FileWriter(fileName, true)); 
            
         writeFile.write("\n" + username + ", " + password); 
         writeFile.close(); 
        
        }
         catch(Exception errorMessage){
             System.out.println("ERROR!, Something went wrong");
         }
                  
    }
   
    /**
     * Add staff details to file
     * @param username parameter for username
     * @param password parameter for password
     * @param email parameter for email
     * @param phone parameter for phone
     * @param fileName parameter for file name
     */
    public void addFullData(String username, String password, String email, 
                                String phone, String fileName){
        
        try{
         BufferedWriter writeFull = new BufferedWriter(new FileWriter(fileName, true)); 
            
         writeFull.write("\n" + username + ", " + password + ", " + email + ", "
                            + phone); 
         writeFull.close(); 
        
        }
         catch(Exception errorMessage){
             System.out.println("ERROR!, Something went wrong");
         } 
        
    } 
     
