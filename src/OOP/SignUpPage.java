     /**
     * Code for create account button 
     * to accept new information
     * @param evt 
     */
    private void btnCreateAccountActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateAccountActionPerformed
        
        String userName = txtUsername.getText();
        String passWord = txtPassword.getText();
        String passWord2 = txtPassword2.getText();
        String phoneNO = txtPhoneNO.getText();
        String eMail = txtEmail.getText();
        
        String invalidCharacters = "[0-9*#+() -]*";
        String emailValidation =  "^[_A-Za-z0-9-\\+]+(\\.[_A-Za-z0-9-]+)*@" + "[A-Za-z0-9-]+(\\.[A-Za-z0-9]+)*(\\.[A-Za-z]{2,})$";
        
        if (phoneNO.equals("") || eMail.equals("")){
           emptyDetails();       
        }
        else if (userName.equals("") || passWord.equals("") ){
           emptyDetails(); 
        }    
        else if(!eMail.matches(emailValidation)){
            invalidEmail();    
        }
        else if(phoneNO.matches(invalidCharacters) && phoneNO.length() != 11){
            invalidPhone();
        }
        else if (passWord.equals(passWord2)){
            itemsPage();
            addTXTfile(userName, passWord, "newCustomers.txt");
            addFullData(userName, passWord, phoneNO, eMail, "fullData.txt");        
        }                 
        else{
            invalidPassword();           
        }
      
    }//GEN-LAST:event_btnCreateAccountActionPerformed

