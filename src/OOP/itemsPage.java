    /**
     * void method to identify if payment is selected
     */
    public void paymentSelected(){
        
        if (cbbPaymentType.getSelectedItem().equals("Card") || cbbPaymentType.getSelectedItem().equals("Cash")){
            System.out.println("Payment Verified");
        }
        else if (cbbPaymentType.getSelectedItem().equals("Payment")){
            emptyPayment();
        }
        else{
            System.out.println("ERROR in payment!");
        }
    }
    
    /**
     * void method to get the change from amount paid
     */
    public void amountChange(){
        DecimalFormat df = new DecimalFormat("0.00");
        
        try{
        double finalTotal = getTotal();
        double amountPaid = getAmount();
        emptyFields();
        paymentSelected();
        
        if (amountPaid == finalTotal || amountPaid > finalTotal){
            double change = amountPaid - finalTotal;
            lblChange.setText("£" + df.format(change));
        }
        else{
            System.out.println("Not enough money");
            noMoney();
        }}
        catch(Exception errorMessage){
            System.out.println("Amount Paid is Empty or is Not a Number");
            //errorMessage.printStackTrace();
        }
    }
    
