    /**
     * void method to determine if input is not numeric
     */
    private void notNumeric(){
              
        String validInput = "^[0-9]*$";
        
        try{
        if (!txtGdonut.getText().matches(validInput) 
            || !txtSdonut.getText().matches(validInput)
            || !txtCdonut.getText().matches(validInput)
            || !txtHotChoco.getText().matches(validInput)
            || !txtChocShake.getText().matches(validInput)
            || !txtStrawShake.getText().matches(validInput)
            || !txtAmountPaid.getText().matches(validInput))
        {
            notNumbers();
        }
        else
        {
            
        }
        }
        catch(Exception errorMessage){
            System.out.println("Input is Not Valid");
        }
        
    }
    
    
